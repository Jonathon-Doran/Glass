#pragma once

#include <stdint.h>

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// ConsumerInterface.h
//
// Defines the interface between GlassVideo and region consumer DLLs.
//
// A consumer DLL exports an IConsumerFactory that GlassVideo calls after
// loading the DLL.  The factory declares which region names it handles and
// creates per-region IFrameConsumer instances.
//
// GlassVideo manages all consumer lifetimes.  The DLL has no dependency on
// GlassVideo internals.
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#define CONSUMER_INTERFACE_VERSION  1

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// RegionFrame
//
// A CPU-side snapshot of a named region's pixel data, delivered to a consumer
// at the consumer's requested interval.  Pixel format is BGRA8.
//
// regionName:  The name of the region this frame was captured from
// pixels:      Pointer to the top-left pixel of the region
// width:       Width of the region in pixels
// height:      Height of the region in pixels
// stride:      Row stride in bytes (may be larger than width * 4)
// frameId:     Monotonically increasing counter; gaps indicate dropped frames
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
struct RegionFrame
{
    const char* regionName;
    const uint8_t* pixels;
    int            width;
    int            height;
    int            stride;
    uint64_t       frameId;
};

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// ConsumerOutputFunc
//
// Callback provided by GlassVideo to each IFrameConsumer instance.
// The consumer calls this to send an arbitrary message back to Glass
// over the notify pipe.  GlassVideo owns the pipe; the consumer has
// no knowledge of it.
//
// message:  A null-terminated UTF-8 string to forward to Glass
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
typedef void (*ConsumerOutputFunc)(const char* message);

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// IFrameConsumer
//
// Per-region consumer instance created by IConsumerFactory::CreateConsumer.
// GlassVideo calls Consume() at the interval declared by GetIntervalMs().
// GlassVideo calls Destroy() when the consumer is no longer needed.
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
struct IFrameConsumer
{
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // GetIntervalMs
    //
    // Returns the minimum delivery interval in milliseconds.
    // GlassVideo will not call Consume() more frequently than this.
    // Return 0 to request delivery on every rendered frame.
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    virtual int GetIntervalMs() const = 0;

    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // Consume
    //
    // Called by GlassVideo with a snapshot of the region's pixel data.
    // The frame pointer is valid only for the duration of this call.
    // The consumer must not retain the pointer.
    //
    // frame:  The region frame snapshot to consume
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    virtual void Consume(const RegionFrame* frame) = 0;

    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // Destroy
    //
    // Called by GlassVideo when this consumer instance is no longer needed.
    // The implementation must free all resources and delete itself.
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    virtual void Destroy() = 0;

protected:
    virtual ~IFrameConsumer() {}
};

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// IConsumerFactory
//
// Exported by the consumer DLL.  GlassVideo calls GetRegionNames() to
// discover which regions the DLL handles, then calls CreateConsumer()
// once per region to instantiate consumers.
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
struct IConsumerFactory
{
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // GetVersion
    //
    // Returns the interface version this DLL was compiled against.
    // GlassVideo will refuse to load a DLL whose version does not match
    // CONSUMER_INTERFACE_VERSION.
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    virtual int GetVersion() const = 0;

    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // GetRegionNames
    //
    // Returns a null-terminated array of region name strings this DLL
    // handles.  The pointers remain valid for the lifetime of the DLL.
    //
    // Example: { "health_bar", "mana_bar", nullptr }
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    virtual const char* const* GetRegionNames() const = 0;

    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // CreateConsumer
    //
    // Instantiates a consumer for the named region.  GlassVideo calls
    // this once per region name returned by GetRegionNames().
    // Returns nullptr if the region is not supported.
    //
    // regionName:  The region this consumer will receive frames for
    // output:      Callback the consumer uses to send messages to Glass
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    virtual IFrameConsumer* CreateConsumer(const char* regionName, ConsumerOutputFunc output) = 0;

    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // Destroy
    //
    // Called by GlassVideo before unloading the DLL.  The implementation
    // must free all factory resources and delete itself.
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    virtual void Destroy() = 0;

protected:
    virtual ~IConsumerFactory() {}
};

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// ConsumerFactory_Create
//
// The single exported C function every consumer DLL must implement.
// GlassVideo calls this immediately after loading the DLL via LoadLibrary.
// Returns a pointer to the factory instance, or nullptr on failure.
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
typedef IConsumerFactory* (*ConsumerFactory_CreateFunc)();
extern "C" __declspec(dllexport) IConsumerFactory* ConsumerFactory_Create();