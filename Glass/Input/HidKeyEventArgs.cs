using Glass.Data.Models;

namespace Glass.Input;

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// HidKeyEventArgs
//
// Arguments for a key state change event from a HID device.
// Device identifies the physical device type.
// KeyName is the key name in Glass convention e.g. "G1", "G15".
// IsPressed is true on key down, false on key up.
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
public class HidKeyEventArgs : EventArgs
{
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // Device
    //
    // The specific device instance that fired this event.
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public HidDeviceInstance? Device { get; set; }

    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // HidKeyEventArgs
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public HidKeyEventArgs(string keyName, bool isPressed)
    {
        KeyName = keyName;
        IsPressed = isPressed;
    }

    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // KeyName
    //
    // The key name in Glass convention e.g. "G1", "G15".
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public string KeyName { get; }

    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // IsPressed
    //
    // True when the key is pressed down, false when released.
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public bool IsPressed { get; }

}
