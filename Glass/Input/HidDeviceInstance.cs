using Glass.Data.Models;

namespace Glass.Input;

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// HidDeviceInstance
//
// Identifies a specific physical HID device instance.
// Type identifies the device type (G13, G15, DominatorX36).
// Instance is assigned at enumeration time — 1 for the first device of a given type,
// 2 for the second, etc. Instance numbering is runtime-only and not persisted.
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
public readonly struct HidDeviceInstance : IEquatable<HidDeviceInstance>
{
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // Type
    //
    // The keyboard device type.
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public KeyboardType Type { get; init; }

    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // Instance
    //
    // The instance number within the device type. 1-based.
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public int Instance { get; init; }

    public string Pid { get; init; }

    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // HidDeviceInstance
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public HidDeviceInstance(KeyboardType type, int instance, string pid)
    {
        Type = type;
        Instance = instance;
        Pid = pid;
    }

    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // Equals
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public bool Equals(HidDeviceInstance other)
    {
        return (Type == other.Type) && (Instance == other.Instance);
    }

    public override bool Equals(object? obj)
    {
        return (obj is HidDeviceInstance other) && Equals(other);
    }

    public override int GetHashCode()
    {
        return HashCode.Combine(Type, Instance);
    }

    public static bool operator ==(HidDeviceInstance left, HidDeviceInstance right)
    {
        return left.Equals(right);
    }

    public static bool operator !=(HidDeviceInstance left, HidDeviceInstance right)
    {
        return !left.Equals(right);
    }

    public override string ToString()
    {
        return $"{Type} Instance {Instance}";
    }
}
