namespace Glass.Network.Protocol.Fields;

///////////////////////////////////////////////////////////////////////////////////////////////
// BagWalker
//
// Iterator over the filled slots of a FieldBag.  Holds its own position so
// multiple walkers can traverse the same bag concurrently.  Produced by
// FieldBag.Walk; not constructed directly by callers.
///////////////////////////////////////////////////////////////////////////////////////////////
public struct BagWalker
{
    private readonly FieldBag _bag;
    private int _position;

    ///////////////////////////////////////////////////////////////////////////////////////////
    // BagWalker (constructor)
    //
    // bag:  The bag to walk.
    ///////////////////////////////////////////////////////////////////////////////////////////
    internal BagWalker(FieldBag bag)
    {
        _bag = bag;
        _position = 0;
    }

    ///////////////////////////////////////////////////////////////////////////////////////////
    // Next
    //
    // Advances past any Empty slots and returns the next filled slot as a
    // FieldBinding.  Returns null when there are no more filled slots.
    ///////////////////////////////////////////////////////////////////////////////////////////
    public FieldBinding? Next()
    {
        return _bag.NextAfter(ref _position);
    }
}
